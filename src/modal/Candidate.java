package modal;
public class Candidate{
	private String userName;
	private String candEnrollId;
	private String party_Affiliation;
	private String aadhar_Number;
	private String gender;
	private String dob;
	private String mobile_No;
	private String email_Id;
	private String user_Password;
	private String address;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCandEnrollId() {
		return candEnrollId;
	}
	public void setCandEnrollId(String candEnrollId) {
		this.candEnrollId = candEnrollId;
	}
	public String getParty_Affiliation() {
		return party_Affiliation;
	}
	public void setParty_Affiliation(String party_Affiliation) {
		this.party_Affiliation = party_Affiliation;
	}
	public String getAadhar_Number() {
		return aadhar_Number;
	}
	public void setAadhar_Number(String aadhar_Number) {
		this.aadhar_Number = aadhar_Number;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getMobile_No() {
		return mobile_No;
	}
	public void setMobile_No(String mobile_No) {
		this.mobile_No = mobile_No;
	}
	public String getEmail_Id() {
		return email_Id;
	}
	public void setEmail_Id(String email_Id) {
		this.email_Id = email_Id;
	}
	public String getUser_Password() {
		return user_Password;
	}
	public void setUser_Password(String user_Password) {
		this.user_Password = user_Password;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}
